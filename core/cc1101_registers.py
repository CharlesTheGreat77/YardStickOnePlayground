def reset_registers_to_default(d):
    '''
    Function for default register values
    '''
    default_registers = [
        (0x0D, 0x45),
        (0x03, 0x0A),
        (0x07, 0x45),
        (0x0B, 0x10),
        (0x0E, 0x75),
        (0x0F, 0x7D),
        (0x10, 0x00),
        (0x11, 0x08),
        (0x12, 0x23),
        (0x18, 0x01),
        (0x19, 0x03),
        (0x1A, 0x05),
        (0x1B, 0x0A),
        (0x1C, 0x11),
        (0x29, 0x00),
        (0x0C, 0x0A),
        (0x1D, 0x01)
    ]

    for reg, value in default_registers:
        d.setRFRegister(reg, value)

